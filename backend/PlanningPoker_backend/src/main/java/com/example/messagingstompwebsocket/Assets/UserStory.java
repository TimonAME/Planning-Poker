package com.example.messagingstompwebsocket.Assets;

public class UserStory {
    public String userStoryHash = "";
    public String title = "";
    public String description = "";

    public String getUserStoryHash() {
        return this.userStoryHash;
    }

    public void setUserStoryHash(String userStoryHash) {
        this.userStoryHash = userStoryHash;
    }

    public String getTitle() {
        return this.userStoryHash;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return this.description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
